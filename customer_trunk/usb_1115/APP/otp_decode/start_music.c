/**@brief   otp音乐播放
   @param 	无
   @return  无
   @note   void music_play_otp(void)
*/
/*----------------------------------------------------------------------------*/
void music_play_otp(void)
{
	u8 key;

	dec_msg = get_dec_msg_ptr();
	SYSTEM_CLK_DIV1();
    flush_all_msg();
	set_max_vol(MAX_ANOLOG_VOL,MAX_DIGITAL_VOL);///设置最大音量	 	
	dac_out_select(DAC_MUSIC, 0);
	start_decode_otp();	
	 	
	while(1)
	{	
	   	key = app_get_msg();

		switch (key)
        {
         case MSG_DECODE_FILE_END:
              stop_decode();
			  work_mode = MUSIC_MODE;
              return;

		 default :
            break;
		}				
	}
}

